public class assign {
    public static void main(String args[]){
        int a=12;
        a+=3;
        System.out.println(a);
        int b=14;
        b-=4;
        System.out.println(b);
        int c=5;
        c*=5;
        System.out.println(c);
        int d=6;
        d%=3;
        System.out.println(d);  // reminder
        int e=2;
        e/=2;
        System.out.println(e); // qution
        System.out.println(12/5-5*4);
        System.out.println((5+3)*(2-8/4)); 
        System.out.println(4*5/2+8-3);
        System.out.println(8-2/6);
        int x=7;
        int y=++x*8;
        System.out.println(y);


    }
    
}
