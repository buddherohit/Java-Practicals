public class resulting {
    public static void main(String args[]){
        short a=10;
        byte b=20;
        System.out.println(a+b);   // short to byte or byte to short = integer value

        int c=10;
        float d=10.5f;
        System.out.println(c+d);  // int to float or float to int = float value 

        char r='a';
        int x=10;
        System.out.println(r+x);  // char to int or int to char = integer 

        double e=1087;
        float f=10.5f;
        System.out.println(e+f);  // double to float or float to double = float 

        long n=987456;
        float m=10.f;
        System.out.println(n+m);  // long to float or float to long = float 

        char p='a';
        short q=10;
        System.out.println(p+q);  // char to short or short to char = int


    }
    
}
